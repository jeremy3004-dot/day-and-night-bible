#!/usr/bin/env node
/**
 * codegen-timestamps.mjs
 *
 * Scans assets/timestamps/ for all generated JSON files and rewrites
 * the TIMESTAMP_REQUIRES map inside src/services/bible/verseTimestamps.ts.
 *
 * Run after generating timestamps:
 *   npm run generate-timestamps -- --translation web
 *   npm run codegen-timestamps
 *
 * Or add to package.json:
 *   "postgenerate-timestamps": "node scripts/codegen-timestamps.mjs"
 */

import { readdir, readFile, writeFile } from 'fs/promises';
import { existsSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const TIMESTAMPS_DIR = path.join(ROOT, 'assets', 'timestamps');
const TARGET_FILE = path.join(ROOT, 'src', 'services', 'bible', 'verseTimestamps.ts');

async function collectEntries() {
  const entries = [];

  if (!existsSync(TIMESTAMPS_DIR)) {
    console.log('No timestamps directory found — nothing to do.');
    return entries;
  }

  const translations = await readdir(TIMESTAMPS_DIR);

  for (const translation of translations.sort()) {
    const translationDir = path.join(TIMESTAMPS_DIR, translation);
    let files;
    try {
      files = await readdir(translationDir);
    } catch {
      continue;
    }

    for (const file of files.sort()) {
      if (!file.endsWith('.json') || file === 'manifest.json') continue;
      const stem = file.replace('.json', ''); // e.g. GEN_001
      const key = `${translation}/${stem}`; // e.g. WEB/GEN_001
      const requirePath = `../../assets/timestamps/${translation}/${file}`;
      entries.push({ key, requirePath });
    }
  }

  return entries;
}

function buildMapBlock(entries) {
  if (entries.length === 0) {
    return `const TIMESTAMP_REQUIRES: Record<string, TimestampRequireFn> = {
  // WEB timestamps — generated by: npm run generate-timestamps -- --translation web
  // BSB timestamps — generated by: npm run generate-timestamps -- --translation bsb
  // Run: npm run codegen-timestamps  to regenerate this map after generating timestamps.
};`;
  }

  // Group by translation
  const byTranslation = {};
  for (const { key, requirePath } of entries) {
    const [trans] = key.split('/');
    if (!byTranslation[trans]) byTranslation[trans] = [];
    byTranslation[trans].push({ key, requirePath });
  }

  const lines = [`const TIMESTAMP_REQUIRES: Record<string, TimestampRequireFn> = {`];

  for (const [trans, items] of Object.entries(byTranslation)) {
    lines.push(`  // ${trans} — ${items.length} chapters`);
    for (const { key, requirePath } of items) {
      lines.push(`  '${key}': () => require('${requirePath}'),`);
    }
  }

  lines.push(`};`);
  return lines.join('\n');
}

async function run() {
  const entries = await collectEntries();
  console.log(`Found ${entries.length} timestamp file(s).`);

  const source = await readFile(TARGET_FILE, 'utf8');

  // Match the existing block — from `const TIMESTAMP_REQUIRES` to the closing `};`
  const startMarker = 'const TIMESTAMP_REQUIRES: Record<string, TimestampRequireFn> = {';
  const startIdx = source.indexOf(startMarker);
  if (startIdx === -1) {
    console.error('ERROR: Could not find TIMESTAMP_REQUIRES in', TARGET_FILE);
    process.exit(1);
  }

  // Find the matching closing `};` after the opening brace
  let depth = 0;
  let endIdx = -1;
  for (let i = startIdx; i < source.length; i++) {
    if (source[i] === '{') depth++;
    if (source[i] === '}') {
      depth--;
      if (depth === 0) {
        // consume the trailing `;` if present
        endIdx = source[i + 1] === ';' ? i + 2 : i + 1;
        break;
      }
    }
  }

  if (endIdx === -1) {
    console.error('ERROR: Could not find closing `};` for TIMESTAMP_REQUIRES');
    process.exit(1);
  }

  const newBlock = buildMapBlock(entries);
  const newSource = source.slice(0, startIdx) + newBlock + source.slice(endIdx);

  await writeFile(TARGET_FILE, newSource, 'utf8');
  console.log(`✅ Updated TIMESTAMP_REQUIRES with ${entries.length} entries in:`);
  console.log(`   ${TARGET_FILE}`);
}

run().catch((err) => {
  console.error(err);
  process.exit(1);
});
